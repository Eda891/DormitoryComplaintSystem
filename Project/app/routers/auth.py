from fastapi import APIRouter, Depends, HTTPException, status
from fastapi.security import OAuth2PasswordRequestForm
from sqlalchemy.orm import Session
from datetime import timedelta
from dependencies import get_db, create_access_token, ACCESS_TOKEN_EXPIRE_MINUTES
from models import User
from schemas import UserCreate, UserResponse, Token
from functions import get_hashed_password, check_password
router = APIRouter()

@router.post("/register",response_model=UserResponse)
def register(user:UserCreate,db:Session=Depends(get_db)):
    db_user=db.query(User).filter(User.userName==user.userName).first()
    if db_user:
            raise HTTPException(status_code=400,detail="Username already registered")
    hashed_password=get_hashed_password(user.password_hash)
    new_user=User(
        studentİd=user.studentİd,
        userName=user.userName,
        email=user.email,
        phone=user.phone,
        roomNum=user.roomNum,
        password_hash=hashed_password  
    )
    db.add(new_user)
    db.commit()
    db.refresh(new_user)
    return new_user
@router.post("/login", response_model=Token)
def login(form_data:OAuth2PasswordRequestForm=Depends(),db:Session=Depends(get_db)):
    user=db.query(User).filter(User.userName==form_data.username).first()
    if not user or not check_password(form_data.password, user.password_hash):
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Incorrect username or password",
            headers={"WWW-Authenticate": "Bearer"},
        )
    access_token_expires = timedelta(minutes=ACCESS_TOKEN_EXPIRE_MINUTES)
    access_token = create_access_token(
        data={"sub": user.userName}, expires_delta=access_token_expires
    )
    return {"access_token": access_token, "token_type": "bearer"}